a, b = 7, 5
print(f"Valor inicial: a = {a}, b = {b}")

# Operador de Igualdad
resultado = a == b
print(f"Resultado de a == b es: {resultado}")

# Operador de Desigualdad
resultado = a != b
print(f"Resultado a != b es: {resultado}")

# Operador mayor que
resultado = a > b
print(f"Resultado a > b es: {resultado}")

# Operador mayor o igual que
resultado = a >= b
print(f"Resultado a >= b es: {resultado}")

# Operador menor que
resultado = a < b
print(f"Resultado a < b es: {resultado}")

# Operador menor o igual que
resultado = a <= b
print(f"Resultado a <= b es: {resultado}")
